        private void printTable(int y, string charLine, RLConsole console)
        {
            int x = 0;
            foreach (char c in charLine)
            {
                if (c == '*')
                {
                    console.Print(x, y, "-", Palette.AgedHardwood);
                }
                else if (c == '/' || c == '\\' || c == '-')
                {
                    console.Print(x, y, c.ToString(), Palette.PlateRim);
                }
                else if (c == ',')
                {
                    console.Print(x, y, c.ToString(), Palette.Plate);
                }
                else if (c == '|')
                {
                    console.Print(x, y, c.ToString(), Palette.Silver);
                }
                ++x;
            }
        }

        private void printTitle(int y, string charLine, RLConsole console)
        {
            int x = 0;
            foreach (char c in charLine)
            {
                if (c == '.' || c == '*')
                {
                    console.Print(x, y, c.ToString(), Palette.OldStone);
                }
                else
                {
                    console.Print(x, y, c.ToString(), Palette.BetterBlood);
                }
                ++x;
            }
        }

        private void printToad(int y, string charLine, RLConsole console)
        {
            int x = 0;
            foreach (char c in charLine)
            {
                if (c == '.' || c == '*')
                {
                    console.Print(x, y, c.ToString(), Palette.OldStone);
                }
                else if (c == '^' || c == '/' || c == '\\' || c == '-' || c == '_')
                {
                    console.Print(x, y, c.ToString(), Palette.DarkGreen);
                }
                else if (c == ',')
                {
                    console.Print(x, y, c.ToString(), Palette.ToadGreen);
                }
                else if (c == '\'')
                {
                    console.Print(x, y, c.ToString(), Palette.Light);
                }
                else if (c == ':')
                {
                    console.Print(x, y, c.ToString(), RLColor.Black);
                }
                else if (c == '+')
                {
                    console.Print(x, y, c.ToString(), Palette.DarkAmber);
                }
                ++x;
            }
        }
